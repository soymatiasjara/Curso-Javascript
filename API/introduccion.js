/*  API
    Application Programming Interface

    1.- APIs del navegador web
    Los navegadores nos dan acceso adiferentes API con las que podemos:
    - Local Storage API - Trabajar con almacenamiento local.
    - Fetch API - Hacer peticiones a servidores.
    - History API - Para acceder al historial de la página.
    - Validation API - Para hacer uso de las funciones del navegador para validar formularios.
    - Más
*/

/*  APIs' Externas
    También puede haber APIs externas que nos permiten acceder a recursos de otra página web.
    Ejemplo con la API de youtube:
    - Nos permite obtener información de videos y canales.
    - Nos permite subir videos.

    Estas son cosas que no podríamos hacer desde nuestra página si no existiera una API.
    Gracias a que existe una API, podemos hablarle y pedirle obtener datos u hacer operaciones.
*/